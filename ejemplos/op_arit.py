#operadores aritméticos
x = 7
y = 2
resultado = x + y  # Suma 9
print(str(resultado) + ' suma')

resultado = x - y  # Resta 5
print(str(resultado) + ' resta')

resultado = x * y  # Producto 14
print(str(resultado) + ' producto')

resultado = x / y  # División 3.5
print(str(resultado) + ' división')

resultado = x % y  # Resto 1
print(str(resultado) + ' resto de la división entera')

resultado = x // y  # Cociente entero 3
print(str(resultado) + ' cociente de la división entera')

resultado=x ** y  # Potencia 49
print(str(resultado) + ' potencia')